<script>

import Nav from "@/components/Nav";
import Footer from "@/components/Footer";

global.jQuery = require('jquery');
var $ = global.jQuery;
var jQuery = global.jQuery;
window.$ = $;
window.jQuery = jQuery;

import {projectCollection} from "@/firebase";

export default {
  name: 'App',
  components: {
    Footer,
    Nav
  },
  data() {
    return {
      projects: []
    }
  },
  methods: {
    async fetchProjects() {
      const snapshot = await projectCollection.get()
      await snapshot.docs.map(doc => {
        this.projects.push(doc.data())
        console.log(doc.data())
      });

      var $workGrid = $('.portfolio-grid').isotope({
        itemSelector: '.all',
        percentPosition: true,
        masonry: {
          columnWidth: '.all'
        }
      });

      $('.portfolio-filter ul li').on('click', function () {

        console.log("clicked");

        $('.portfolio-filter ul li').removeClass('active');
        $(this).addClass('active');

        var data = $(this).attr('data-filter');
        $workGrid.isotope({
          filter: data
        });
      });

      setTimeout(function (){
        $('.refresh').trigger("click");
      }, 500)

    }
  },
  created() {
    this.fetchProjects()
  },
  mounted() {
    window.document.onscroll = () => {
      let nav_offset_top = $('header').height() + 50;

      var scroll = $(window).scrollTop();
      if (scroll >= nav_offset_top) {
        $('.header_area').addClass('navbar_fixed');
      } else {
        $('.header_area').removeClass('navbar_fixed');
      }


      //nav match
      $('.content_section').each(function () {
        var sectionName = $(this).attr("id");
        var navMatch = $('.nav-item a[href="#' + sectionName + '"]')

        // console.log(sectionName);
        // console.log(navMatch);

        if ($(this).offset().top - $(window).height() / 2 < $(window).scrollTop() &&
            $(this).offset().top + $(this).height() - $(window).height() / 2 > $(window).scrollTop()) {
          navMatch.parent().addClass("active")
        } else {
          navMatch.parent().removeClass("active")
        }

      })

    }

    if ($('.testi_slider').length) {
      $('.testi_slider').owlCarousel({
        loop: true,
        margin: 30,
        items: 2,
        autoplay: true,
        smartSpeed: 2500,
        dots: true,
        responsiveClass: true,
        responsive: {
          0: {
            items: 1
          },
          991: {
            items: 2
          }
        }
      });
    }

    $('.nav-link').on('click', function (event) {
      if (this.hash !== "") {
        event.preventDefault();
        var hash = this.hash

        $('html, body').animate({
          scrollTop: $(hash).offset().top
        }, 500, function () {

          // Add hash (#) to URL when done scrolling (default click behavior)
          window.location.hash = hash;
        });

      }
    });

  }
}
